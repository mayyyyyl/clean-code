package ex1;

public class Calcul {

    /**
     * Cette méthode retourne la somme de deux entiers.
     *
     * @param a Le premier entier
     * @param b Le deuxième entier
     * @return La somme des deux entiers
     */
    public int additionner(int a, int b) {
        return a + b;
    }

    /**
     * Cette méthode retourne la différence entre deux entiers.
     *
     * @param a Le premier entier
     * @param b Le deuxième entier
     * @return La différence entre les deux entiers
     */
    public int soustraire(int a, int b) {
        return a - b;
    }
}
